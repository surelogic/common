CREATE TABLE PAYPAL_TXN (
       TXN_ID              CHAR(19)     PRIMARY KEY,
       DATE_TIME           TIMESTAMP    NOT NULL,
       ITEM_NAME           CHAR(127)    NOT NULL,
       ITEM_NUMBER         CHAR(127)    NOT NULL,
       ITEM_OPTION         VARCHAR(500),
       PAYMENT_STATUS      VARCHAR(17)  NOT NULL CONSTRAINT P_T_STATUS_CN CHECK (PAYMENT_STATUS IN ('Canceled_Reversal','Completed','Created','Denied','Expired','Failed','Pending','Refunded','Reversed','Processed','Voided')),
       PAYMENT_AMOUNT      VARCHAR(10)  NOT NULL,
       PAYER_EMAIL         VARCHAR(127) NOT NULL,
       PAYER_ID            VARCHAR(127) NOT NULL,
       RECEIVER_EMAIL      VARCHAR(127) NOT NULL,
       SENDTO_EMAIL        VARCHAR(320),
       ADDRESS_COUNTRY     VARCHAR(64),
       ADDRESS_CITY        VARCHAR(40),
       ADDRESS_CC          CHAR(2),
       ADDRESS_NAME        VARCHAR(128),
       ADDRESS_STATE       VARCHAR(40),
       ADDRESS_STATUS      VARCHAR(11) CONSTRAINT P_T_ADDRESS_STATUS_CN CHECK (ADDRESS_STATUS IN ('confirmed','unconfirmed')),
       ADDRESS_STREET      VARCHAR(200),
       ADDRESS_ZIP         VARCHAR(20),
       CONTACT_PHONE       VARCHAR(20),
       FIRST_NAME          VARCHAR(64),
       LAST_NAME           VARCHAR(64),
       PAYER_BUSINESS_NAME VARCHAR(127)
)
